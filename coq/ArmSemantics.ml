(** val compute_n_flag : I32.int -> bool **)

let compute_n_flag result =
  Z.ltb (I32.signed result) 0

(** val compute_z_flag : I32.int -> bool **)

let compute_z_flag result =
  I32.eq result I32.zero

(** val compute_c_flag_add : I32.int -> I32.int -> bool **)

let compute_c_flag_add x y =
  let ux = I32.unsigned x in
  let uy = I32.unsigned y in Z.ltb I32.max_unsigned (Z.add ux uy)

(** val compute_v_flag_add : I32.int -> I32.int -> I32.int -> bool **)

let compute_v_flag_add x y result =
  let sx = I32.signed x in
  let sy = I32.signed y in
  let sr = I32.signed result in
  (||) ((&&) ((&&) (Z.leb 0 sx) (Z.leb 0 sy)) (Z.ltb sr 0))
    ((&&) ((&&) (Z.ltb sx 0) (Z.ltb sy 0)) (Z.leb 0 sr))

(** val compute_c_flag_sub : I32.int -> I32.int -> bool **)

let compute_c_flag_sub v1 v2 =
  Z.leb (I32.unsigned v2) (I32.unsigned v1)

(** val compute_v_flag_sub : I32.int -> I32.int -> bool **)

let compute_v_flag_sub v1 v2 =
  let sv1 = I32.signed v1 in
  let sv2 = I32.signed v2 in
  let sr = I32.signed (I32.sub v1 v2) in
  (||) ((&&) ((&&) (Z.leb 0 sv1) (Z.ltb sv2 0)) (Z.ltb sr 0))
    ((&&) ((&&) (Z.ltb sv1 0) (Z.leb 0 sv2)) (Z.leb 0 sr))

(** val compute_c_flag_sbc : I32.int -> I32.int -> bool -> bool **)

let compute_c_flag_sbc v1 v2 carry_in =
  let borrow = if carry_in then 0 else 1 in
  Z.leb (Z.add (I32.unsigned v2) borrow) (I32.unsigned v1)

(** val compute_v_flag_sbc : I32.int -> I32.int -> bool -> bool **)

let compute_v_flag_sbc v1 v2 carry_in =
  let borrow = if carry_in then 0 else 1 in
  let borrow_in = if carry_in then I32.zero else I32.one in
  let exact = Z.sub (Z.sub (I32.signed v1) (I32.signed v2)) borrow in
  negb (Z.eqb (I32.signed (I32.sub (I32.sub v1 v2) borrow_in)) exact)

(** val update_flags_arith : I32.int -> bool -> bool -> condition_flags **)

let update_flags_arith result c v =
  { flag_n = (compute_n_flag result); flag_z = (compute_z_flag result);
    flag_c = c; flag_v = v }

(** val exec_instr : arm_instr -> arm_state -> arm_state option **)

let exec_instr i s =
  match i with
  | ADD (rd, rn, op2) ->
    let v1 = get_reg s rn in
    let v2 = eval_operand2 op2 s in
    let result = I32.add v1 v2 in Some (set_reg s rd result)
  | SUB (rd, rn, op2) ->
    let v1 = get_reg s rn in
    let v2 = eval_operand2 op2 s in
    let result = I32.sub v1 v2 in Some (set_reg s rd result)
  | MUL (rd, rn, rm) ->
    let v1 = get_reg s rn in
    let v2 = get_reg s rm in
    let result = I32.mul v1 v2 in Some (set_reg s rd result)
  | SDIV (rd, rn, rm) ->
    let v1 = get_reg s rn in
    let v2 = get_reg s rm in
    (match I32.divs v1 v2 with
     | Some result -> Some (set_reg s rd result)
     | None -> None)
  | UDIV (rd, rn, rm) ->
    let v1 = get_reg s rn in
    let v2 = get_reg s rm in
    (match I32.divu v1 v2 with
     | Some result -> Some (set_reg s rd result)
     | None -> None)
  | MLS (rd, rn, rm, ra) ->
    let vn = get_reg s rn in
    let vm = get_reg s rm in
    let va = get_reg s ra in
    let product = I32.mul vn vm in
    let result = I32.sub va product in Some (set_reg s rd result)
  | ADDS (rd, rn, op2) ->
    let v1 = get_reg s rn in
    let v2 = eval_operand2 op2 s in
    let result = I32.add v1 v2 in
    let c = compute_c_flag_add v1 v2 in
    let v = compute_v_flag_add v1 v2 result in
    let new_flags = update_flags_arith result c v in
    Some (set_flags (set_reg s rd result) new_flags)
  | ADC (rd, rn, op2) ->
    let v1 = get_reg s rn in
    let v2 = eval_operand2 op2 s in
    let carry_in = if s.flags.flag_c then I32.one else I32.zero in
    let result = I32.add (I32.add v1 v2) carry_in in
    Some (set_reg s rd result)
  | SUBS (rd, rn, op2) ->
    let v1 = get_reg s rn in
    let v2 = eval_operand2 op2 s in
    let result = I32.sub v1 v2 in
    let c = compute_c_flag_sub v1 v2 in
    let v = compute_v_flag_sub v1 v2 in
    let new_flags = update_flags_arith result c v in
    Some (set_flags (set_reg s rd result) new_flags)
  | SBC (rd, rn, op2) ->
    let v1 = get_reg s rn in
    let v2 = eval_operand2 op2 s in
    let borrow_in = if s.flags.flag_c then I32.zero else I32.one in
    let result = I32.sub (I32.sub v1 v2) borrow_in in
    Some (set_reg s rd result)
  | SBCS (rd, rn, op2) ->
    let v1 = get_reg s rn in
    let v2 = eval_operand2 op2 s in
    let cin = s.flags.flag_c in
    let borrow_in = if cin then I32.zero else I32.one in
    let result = I32.sub (I32.sub v1 v2) borrow_in in
    let c = compute_c_flag_sbc v1 v2 cin in
    let v = compute_v_flag_sbc v1 v2 cin in
    let new_flags = update_flags_arith result c v in
    Some (set_flags (set_reg s rd result) new_flags)
  | AND (rd, rn, op2) ->
    let v1 = get_reg s rn in
    let v2 = eval_operand2 op2 s in
    let result = I32.coq_and v1 v2 in Some (set_reg s rd result)
  | ORR (rd, rn, op2) ->
    let v1 = get_reg s rn in
    let v2 = eval_operand2 op2 s in
    let result = I32.coq_or v1 v2 in Some (set_reg s rd result)
  | EOR (rd, rn, op2) ->
    let v1 = get_reg s rn in
    let v2 = eval_operand2 op2 s in
    let result = I32.xor v1 v2 in Some (set_reg s rd result)
  | MVN (rd, op2) ->
    let v = eval_operand2 op2 s in
    let result = I32.repr (Z.lnot (I32.unsigned v)) in
    Some (set_reg s rd result)
  | LSL (rd, rn, shift) ->
    let v = get_reg s rn in
    let shift_amt = I32.repr (Z.of_nat shift) in
    let result = I32.shl v shift_amt in Some (set_reg s rd result)
  | LSR (rd, rn, shift) ->
    let v = get_reg s rn in
    let shift_amt = I32.repr (Z.of_nat shift) in
    let result = I32.shru v shift_amt in Some (set_reg s rd result)
  | ASR (rd, rn, shift) ->
    let v = get_reg s rn in
    let shift_amt = I32.repr (Z.of_nat shift) in
    let result = I32.shrs v shift_amt in Some (set_reg s rd result)
  | ROR (rd, rn, shift) ->
    let v = get_reg s rn in
    let shift_amt = I32.repr (Z.of_nat shift) in
    let result = I32.rotr v shift_amt in Some (set_reg s rd result)
  | LSL_reg (rd, rn, rm) ->
    let v = get_reg s rn in
    let shift_amt = get_reg s rm in Some (set_reg s rd (I32.shl v shift_amt))
  | LSR_reg (rd, rn, rm) ->
    let v = get_reg s rn in
    let shift_amt = get_reg s rm in Some (set_reg s rd (I32.shru v shift_amt))
  | ASR_reg (rd, rn, rm) ->
    let v = get_reg s rn in
    let shift_amt = get_reg s rm in Some (set_reg s rd (I32.shrs v shift_amt))
  | ROR_reg (rd, rn, rm) ->
    let v = get_reg s rn in
    let shift_amt = get_reg s rm in Some (set_reg s rd (I32.rotr v shift_amt))
  | RSB (rd, rn, op2) ->
    let v1 = get_reg s rn in
    let v2 = eval_operand2 op2 s in Some (set_reg s rd (I32.sub v2 v1))
  | MOV (rd, op2) -> let v = eval_operand2 op2 s in Some (set_reg s rd v)
  | MOVW (rd, imm) -> Some (set_reg s rd imm)
  | MOVT (rd, imm) ->
    let current = get_reg s rd in
    let lower =
      I32.coq_and current
        (I32.repr ((fun p->1+2*p) ((fun p->1+2*p) ((fun p->1+2*p)
          ((fun p->1+2*p) ((fun p->1+2*p) ((fun p->1+2*p) ((fun p->1+2*p)
          ((fun p->1+2*p) ((fun p->1+2*p) ((fun p->1+2*p) ((fun p->1+2*p)
          ((fun p->1+2*p) ((fun p->1+2*p) ((fun p->1+2*p) ((fun p->1+2*p)
          1))))))))))))))))
    in
    let upper =
      I32.shl imm
        (I32.repr ((fun p->2*p) ((fun p->2*p) ((fun p->2*p) ((fun p->2*p)
          1)))))
    in
    let result = I32.coq_or lower upper in Some (set_reg s rd result)
  | MOVEQ (rd, op2) ->
    if s.flags.flag_z
    then let v = eval_operand2 op2 s in Some (set_reg s rd v)
    else Some s
  | MOVNE (rd, op2) ->
    if s.flags.flag_z
    then Some s
    else let v = eval_operand2 op2 s in Some (set_reg s rd v)
  | MOVLT (rd, op2) ->
    if eqb s.flags.flag_n s.flags.flag_v
    then Some s
    else let v = eval_operand2 op2 s in Some (set_reg s rd v)
  | MOVLE (rd, op2) ->
    if (||) s.flags.flag_z (negb (eqb s.flags.flag_n s.flags.flag_v))
    then let v = eval_operand2 op2 s in Some (set_reg s rd v)
    else Some s
  | MOVGT (rd, op2) ->
    if (&&) (negb s.flags.flag_z) (eqb s.flags.flag_n s.flags.flag_v)
    then let v = eval_operand2 op2 s in Some (set_reg s rd v)
    else Some s
  | MOVGE (rd, op2) ->
    if eqb s.flags.flag_n s.flags.flag_v
    then let v = eval_operand2 op2 s in Some (set_reg s rd v)
    else Some s
  | MOVLO (rd, op2) ->
    if s.flags.flag_c
    then Some s
    else let v = eval_operand2 op2 s in Some (set_reg s rd v)
  | MOVLS (rd, op2) ->
    if (||) (negb s.flags.flag_c) s.flags.flag_z
    then let v = eval_operand2 op2 s in Some (set_reg s rd v)
    else Some s
  | MOVHI (rd, op2) ->
    if (&&) s.flags.flag_c (negb s.flags.flag_z)
    then let v = eval_operand2 op2 s in Some (set_reg s rd v)
    else Some s
  | MOVHS (rd, op2) ->
    if s.flags.flag_c
    then let v = eval_operand2 op2 s in Some (set_reg s rd v)
    else Some s
  | CMP (rn, op2) ->
    let v1 = get_reg s rn in
    let v2 = eval_operand2 op2 s in
    let result = I32.sub v1 v2 in
    let c = compute_c_flag_sub v1 v2 in
    let v = compute_v_flag_sub v1 v2 in
    let new_flags = update_flags_arith result c v in
    Some (set_flags s new_flags)
  | CMPCond (cond, rn, op2) ->
    if eval_condition cond s.flags
    then let v1 = get_reg s rn in
         let v2 = eval_operand2 op2 s in
         let result = I32.sub v1 v2 in
         let c = compute_c_flag_sub v1 v2 in
         let v = compute_v_flag_sub v1 v2 in
         let new_flags = update_flags_arith result c v in
         Some (set_flags s new_flags)
    else Some s
  | CLZ (rd, rm) -> let v = get_reg s rm in Some (set_reg s rd (I32.clz v))
  | RBIT (rd, rm) -> let v = get_reg s rm in Some (set_reg s rd (I32.rbit v))
  | POPCNT (rd, rm) ->
    let v = get_reg s rm in Some (set_reg s rd (I32.popcnt v))
  | LDR (rd, rn, offset) ->
    let base = get_reg s rn in
    let addr = I32.add base (I32.repr offset) in
    let value = load_mem s (I32.signed addr) in Some (set_reg s rd value)
  | STR (rd, rn, offset) ->
    let base = get_reg s rn in
    let addr = I32.add base (I32.repr offset) in
    let value = get_reg s rd in Some (store_mem s (I32.signed addr) value)
  | UDF _ -> None
  | CMN (rn, op2) ->
    let v1 = get_reg s rn in
    let v2 = eval_operand2 op2 s in
    let result = I32.add v1 v2 in
    let c = compute_c_flag_add v1 v2 in
    let v = compute_v_flag_add v1 v2 result in
    let new_flags = update_flags_arith result c v in
    Some (set_flags s new_flags)
  | B offset ->
    let current_pc = get_reg s PC in
    let new_pc = I32.add current_pc (I32.repr offset) in
    Some (set_reg s PC new_pc)
  | BL offset ->
    let current_pc = get_reg s PC in
    let return_addr =
      I32.add current_pc (I32.repr ((fun p->2*p) ((fun p->2*p) 1)))
    in
    let s' = set_reg s LR return_addr in
    let new_pc = I32.add current_pc (I32.repr offset) in
    Some (set_reg s' PC new_pc)
  | BX rm -> let target = get_reg s rm in Some (set_reg s PC target)
  | BCondOffset (_, _) -> Some s
  | VADD_F32 (sd, sn, sm) ->
    let vn = get_vfp_reg s sn in
    let vm = get_vfp_reg s sm in Some (set_vfp_reg s sd (f32_add_bits vn vm))
  | VSUB_F32 (sd, sn, sm) ->
    let vn = get_vfp_reg s sn in
    let vm = get_vfp_reg s sm in Some (set_vfp_reg s sd (f32_sub_bits vn vm))
  | VMUL_F32 (sd, sn, sm) ->
    let vn = get_vfp_reg s sn in
    let vm = get_vfp_reg s sm in Some (set_vfp_reg s sd (f32_mul_bits vn vm))
  | VDIV_F32 (sd, sn, sm) ->
    let vn = get_vfp_reg s sn in
    let vm = get_vfp_reg s sm in Some (set_vfp_reg s sd (f32_div_bits vn vm))
  | VSQRT_F32 (sd, sm) ->
    let vm = get_vfp_reg s sm in Some (set_vfp_reg s sd (f32_sqrt_bits vm))
  | VABS_F32 (sd, sm) ->
    let vm = get_vfp_reg s sm in Some (set_vfp_reg s sd (f32_abs_bits vm))
  | VNEG_F32 (sd, sm) ->
    let vm = get_vfp_reg s sm in Some (set_vfp_reg s sd (f32_neg_bits vm))
  | VADD_F64 (dd, dn, dm) ->
    let vn = get_vfp_reg s dn in
    let vm = get_vfp_reg s dm in Some (set_vfp_reg s dd (f64_add_bits vn vm))
  | VSUB_F64 (dd, dn, dm) ->
    let vn = get_vfp_reg s dn in
    let vm = get_vfp_reg s dm in Some (set_vfp_reg s dd (f64_sub_bits vn vm))
  | VMUL_F64 (dd, dn, dm) ->
    let vn = get_vfp_reg s dn in
    let vm = get_vfp_reg s dm in Some (set_vfp_reg s dd (f64_mul_bits vn vm))
  | VDIV_F64 (dd, dn, dm) ->
    let vn = get_vfp_reg s dn in
    let vm = get_vfp_reg s dm in Some (set_vfp_reg s dd (f64_div_bits vn vm))
  | VSQRT_F64 (dd, dm) ->
    let vm = get_vfp_reg s dm in Some (set_vfp_reg s dd (f64_sqrt_bits vm))
  | VABS_F64 (dd, dm) ->
    let vm = get_vfp_reg s dm in Some (set_vfp_reg s dd (f64_abs_bits vm))
  | VNEG_F64 (dd, dm) ->
    let vm = get_vfp_reg s dm in Some (set_vfp_reg s dd (f64_neg_bits vm))
  | VCMP_F32 (sn, sm) ->
    let vn = get_vfp_reg s sn in
    let vm = get_vfp_reg s sm in Some (set_flags s (f32_compare_flags vn vm))
  | VCMP_F64 (dn, dm) ->
    let vn = get_vfp_reg s dn in
    let vm = get_vfp_reg s dm in Some (set_flags s (f64_compare_flags vn vm))
  | VCVT_F32_F64 (sd, dm) ->
    let vm = get_vfp_reg s dm in
    Some (set_vfp_reg s sd (cvt_f64_to_f32_bits vm))
  | VCVT_F64_F32 (dd, sm) ->
    let vm = get_vfp_reg s sm in
    Some (set_vfp_reg s dd (cvt_f32_to_f64_bits vm))
  | VCVT_S32_F32 (sd, sm) ->
    let vm = get_vfp_reg s sm in
    Some (set_vfp_reg s sd (cvt_f32_to_s32_bits vm))
  | VCVT_F32_S32 (sd, sm) ->
    let vm = get_vfp_reg s sm in
    Some (set_vfp_reg s sd (cvt_s32_to_f32_bits vm))
  | VMOV (sd, sm) -> let vm = get_vfp_reg s sm in Some (set_vfp_reg s sd vm)
  | VMOV_ARM_TO_VFP (sd, rm) ->
    let vm = get_reg s rm in Some (set_vfp_reg s sd vm)
  | VMOV_VFP_TO_ARM (rd, sm) ->
    let vm = get_vfp_reg s sm in Some (set_reg s rd vm)
  | VLDR_F32 (sd, rn, offset) ->
    let base = get_reg s rn in
    let addr = I32.add base (I32.repr offset) in
    let value = load_mem s (I32.signed addr) in Some (set_vfp_reg s sd value)
  | VSTR_F32 (sd, rn, offset) ->
    let base = get_reg s rn in
    let addr = I32.add base (I32.repr offset) in
    let value = get_vfp_reg s sd in Some (store_mem s (I32.signed addr) value)
  | VLDR_F64 (dd, rn, offset) ->
    let base = get_reg s rn in
    let addr = I32.add base (I32.repr offset) in
    let value = load_mem s (I32.signed addr) in Some (set_vfp_reg s dd value)
  | VSTR_F64 (dd, rn, offset) ->
    let base = get_reg s rn in
    let addr = I32.add base (I32.repr offset) in
    let value = get_vfp_reg s dd in Some (store_mem s (I32.signed addr) value)
  | I64MulPseudo (rdlo, rdhi, rnlo, rnhi, rmlo, rmhi) ->
    let vnlo = get_reg s rnlo in
    let vnhi = get_reg s rnhi in
    let vmlo = get_reg s rmlo in
    let vmhi = get_reg s rmhi in
    let lo = i64_mul_lo_bits vnlo vnhi vmlo vmhi in
    let hi = i64_mul_hi_bits vnlo vnhi vmlo vmhi in
    Some (set_reg (set_reg s rdlo lo) rdhi hi)
  | I64ShlPseudo (rdlo, rdhi, rnlo, rnhi, rmlo, _) ->
    let vnlo = get_reg s rnlo in
    let vnhi = get_reg s rnhi in
    let cnt = get_reg s rmlo in
    let lo = i64_shl_lo_bits vnlo vnhi cnt in
    let hi = i64_shl_hi_bits vnlo vnhi cnt in
    Some (set_reg (set_reg s rdlo lo) rdhi hi)
  | I64ShrUPseudo (rdlo, rdhi, rnlo, rnhi, rmlo, _) ->
    let vnlo = get_reg s rnlo in
    let vnhi = get_reg s rnhi in
    let cnt = get_reg s rmlo in
    let lo = i64_shru_lo_bits vnlo vnhi cnt in
    let hi = i64_shru_hi_bits vnlo vnhi cnt in
    Some (set_reg (set_reg s rdlo lo) rdhi hi)
  | I64ShrSPseudo (rdlo, rdhi, rnlo, rnhi, rmlo, _) ->
    let vnlo = get_reg s rnlo in
    let vnhi = get_reg s rnhi in
    let cnt = get_reg s rmlo in
    let lo = i64_shrs_lo_bits vnlo vnhi cnt in
    let hi = i64_shrs_hi_bits vnlo vnhi cnt in
    Some (set_reg (set_reg s rdlo lo) rdhi hi)
  | I64RotlPseudo (rdlo, rdhi, rnlo, rnhi, shift) ->
    let vnlo = get_reg s rnlo in
    let vnhi = get_reg s rnhi in
    let cnt = get_reg s shift in
    let lo = i64_rotl_lo_bits vnlo vnhi cnt in
    let hi = i64_rotl_hi_bits vnlo vnhi cnt in
    Some (set_reg (set_reg s rdlo lo) rdhi hi)
  | I64RotrPseudo (rdlo, rdhi, rnlo, rnhi, shift) ->
    let vnlo = get_reg s rnlo in
    let vnhi = get_reg s rnhi in
    let cnt = get_reg s shift in
    let lo = i64_rotr_lo_bits vnlo vnhi cnt in
    let hi = i64_rotr_hi_bits vnlo vnhi cnt in
    Some (set_reg (set_reg s rdlo lo) rdhi hi)
  | I64ClzPseudo (rd, rnlo, rnhi) ->
    let vnlo = get_reg s rnlo in
    let vnhi = get_reg s rnhi in Some (set_reg s rd (i64_clz_bits vnlo vnhi))
  | I64CtzPseudo (rd, rnlo, rnhi) ->
    let vnlo = get_reg s rnlo in
    let vnhi = get_reg s rnhi in Some (set_reg s rd (i64_ctz_bits vnlo vnhi))
  | I64PopcntPseudo (rd, rnlo, rnhi) ->
    let vnlo = get_reg s rnlo in
    let vnhi = get_reg s rnhi in
    Some (set_reg s rd (i64_popcnt_bits vnlo vnhi))
  | I64SetCond (rd, rnlo, rnhi, rmlo, rmhi, cond) ->
    let vnlo = get_reg s rnlo in
    let vnhi = get_reg s rnhi in
    let vmlo = get_reg s rmlo in
    let vmhi = get_reg s rmhi in
    Some (set_reg s rd (i64_setcond_bits vnlo vnhi vmlo vmhi cond))
  | I64SetCondZ (rd, rnlo, rnhi) ->
    let vnlo = get_reg s rnlo in
    let vnhi = get_reg s rnhi in
    Some (set_reg s rd (i64_setcondz_bits vnlo vnhi))
  | I64DivSPseudo (rdlo, rdhi, rnlo, rnhi, rmlo, rmhi) ->
    let vnlo = get_reg s rnlo in
    let vnhi = get_reg s rnhi in
    let vmlo = get_reg s rmlo in
    let vmhi = get_reg s rmhi in
    (match i64_divs_pair vnlo vnhi vmlo vmhi with
     | Some p -> let lo,hi = p in Some (set_reg (set_reg s rdlo lo) rdhi hi)
     | None -> None)
  | I64DivUPseudo (rdlo, rdhi, rnlo, rnhi, rmlo, rmhi) ->
    let vnlo = get_reg s rnlo in
    let vnhi = get_reg s rnhi in
    let vmlo = get_reg s rmlo in
    let vmhi = get_reg s rmhi in
    (match i64_divu_pair vnlo vnhi vmlo vmhi with
     | Some p -> let lo,hi = p in Some (set_reg (set_reg s rdlo lo) rdhi hi)
     | None -> None)
  | I64RemSPseudo (rdlo, rdhi, rnlo, rnhi, rmlo, rmhi) ->
    let vnlo = get_reg s rnlo in
    let vnhi = get_reg s rnhi in
    let vmlo = get_reg s rmlo in
    let vmhi = get_reg s rmhi in
    (match i64_rems_pair vnlo vnhi vmlo vmhi with
     | Some p -> let lo,hi = p in Some (set_reg (set_reg s rdlo lo) rdhi hi)
     | None -> None)
  | I64RemUPseudo (rdlo, rdhi, rnlo, rnhi, rmlo, rmhi) ->
    let vnlo = get_reg s rnlo in
    let vnhi = get_reg s rnhi in
    let vmlo = get_reg s rmlo in
    let vmhi = get_reg s rmhi in
    (match i64_remu_pair vnlo vnhi vmlo vmhi with
     | Some p -> let lo,hi = p in Some (set_reg (set_reg s rdlo lo) rdhi hi)
     | None -> None)
  | I64ConstPseudo (rdlo, rdhi, v) ->
    Some (set_reg (set_reg s rdlo (i64_const_lo v)) rdhi (i64_const_hi v))
  | I64ExtendI32SPseudo (rdlo, rdhi, rn) ->
    let v = get_reg s rn in
    Some (set_reg (set_reg s rdlo v) rdhi (i64_extend_s_hi v))
  | I64ExtendI32UPseudo (rdlo, rdhi, rn) ->
    let v = get_reg s rn in Some (set_reg (set_reg s rdlo v) rdhi I32.zero)
  | I32WrapI64Pseudo (rd, rnlo) ->
    let v = get_reg s rnlo in Some (set_reg s rd v)
  | I64LoadPseudo (rdlo, rdhi, addr, offset) ->
    let base = get_reg s addr in
    let a = I32.signed (I32.add base (I32.repr offset)) in
    Some
    (set_reg (set_reg s rdlo (i64_load_lo s.mem a)) rdhi
      (i64_load_hi s.mem a))
  | I64StorePseudo (rnlo, rnhi, addr, offset) ->
    let base = get_reg s addr in
    let a = I32.signed (I32.add base (I32.repr offset)) in
    let lo = get_reg s rnlo in
    let hi = get_reg s rnhi in
    Some
    (store_mem (store_mem s a lo) (Z.add a ((fun p->2*p) ((fun p->2*p) 1)))
      hi)

(** val exec_program : arm_instr list -> arm_state -> arm_state option **)

let rec exec_program prog s =
  match prog with
  | [] -> Some s
  | i::rest ->
    (match exec_instr i s with
     | Some s' -> exec_program rest s'
     | None -> None)
